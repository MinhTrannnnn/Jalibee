package model;

/**
 * Lớp Product đại diện cho thông tin của một sản phẩm, bao gồm mã sản phẩm, tên, giá, và hình ảnh.
 */
public class Product {
    private int pid;
    private String pname;
    private double price;
    private byte[] image;

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }


    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public byte[] getImage() {
        return image;
    }

    public void setImage(byte[] image) {
        this.image = image;
    }
}
